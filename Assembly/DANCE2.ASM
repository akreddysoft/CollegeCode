;VERY IMPORTANT PROGRAM
;WHILE PRINTING A STRING THE DS MUST BE SET TO DATA SEGMENT
TITLE IMPKEYB
BIODATA SEGMENT AT 0B000H
ORG 8001H
KBSTATE DB ?
BIODATA ENDS

CODESG SEGMENT PARA
ASSUME CS:CODESG,DS:BIODATA
ORG 100H
MAIN:
JMP BEGIN
MSG DB 'MADHUR$'
BEGIN:
MOV AX,BIODATA
MOV DS,AX      ;DS CONTAINS ADDRESS OF KEYBOARD BYTE NOT DATA SEGMENT
RUN:
MOV CX,4000H
;MOV AL,KBSTATE
MOV [KBSTATE],0003H
INC KBSTATE
INC KBSTATE
LOOP RUN
QUIT:
MOV AH,4CH
INT 21H
CODESG ENDS
END MAIN