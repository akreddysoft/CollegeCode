;SEGMENTS ARE ALIGNED ON WORD BOUNDARY
;FIRST CODE AND THEN DATA AND THEN STACK
;FIRST OF THE CONCEPT SERIES
;FOCUSES ON ALIGNMENT OF SEGMENTS.
TITLE CONCEPT
.MODEL SMALL
.STACK 1024H
.DATA
MA DB 'MADHUR'
ITEMA DB 129
DATE DB 'WHAT IS THE DATE$'
.CODE
START:
	MOV AX,@DATA
	MOV DS,AX
	MOV AX,OFFSET MA
	MOV CH,ITEMA
	MOV DX,OFFSET  ITEMA
	MOV AH,09H
	LEA DX,DATE
	INT 21H
	.EXIT
END START