TITLE ADDMULSUB
.MODEL SMALL
.STACK
.DATA
A1	 DB 60H         ;WE WANT TO ADD 20H TO 60H
A3	 DW 0123H
A2	 DW 0BC62H      ;WE WANT TO ADD 123H TO BC62H
A4	 DW 0012H
A5	 DW 553AH	;WE WANT TO ADD 0123 BC62 AND 0012 553A
.CODE
START:
MOV	AX,@DATA
MOV	DS,AX
MOV	AH,A1
ADD	AH,20H ;this addition sets sign and overflow flag

MOV	AL,A1
CBW
ADD	AX,20H ;THIS ADDTION CLEARS ABOVE FLAG,NO OVERFLOW IS THERE

MOV	AX,A2
ADD	AX,A3  ;THIS ADDITION ONLY SETS THE SIGN


.EXIT
END	START

